package com.cybertek.java.ShorrthanOperators;

public class Shorthand {
    public static void main( String [] args){
        int count = 22;
        // increse the count by 5;
        count = count+5;
        System.out.println(count);
        count *=5;
        System.out.println(count);
        count/=5;

        System.out.println(count);
        count%=5;
        System.out.println(count);

    }
}

